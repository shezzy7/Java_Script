// unshift : add an element at the beginning
//syntax-> arrayName.unshift(element);
let arr=["jam","shahzad","huusain"];
console.log(arr);
arr.unshift("Muhammad");
console.log(arr);
arr.unshift("ibneAdam");
console.log(arr);
//shift: removes an element from the beginning
//syntax-> arrayName.shift();
arr.shift();
console.log(arr);
let brr=arr.shift();
console.log(brr);
console.log(arr);