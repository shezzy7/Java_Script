//Nested array mean array of arrays or multi-dimensional array
let nest=[[1,2],[3,4],[5,6,8]];
//to print main array
console.log(`nest = ${nest}`);
//to print any specific inner array of main array
console.log(`nest[0] = ${nest[0]}`);
console.log(`nest[1] = ${nest[1]}`);
console.log(`nest[2] = ${nest[2]}`);
//to print specific elements of inner arrays of main array
console.log(`nest[0][0] = ${nest[0][0]}`);
console.log(`nest[0][1] = ${nest[0][1]}`);
console.log(`nest[1][0] = ${nest[1][0]}`);
console.log(`nest[1][1] = ${nest[1][1]}`);
console.log(`nest[2][0] = ${nest[2][0]}`);
console.log(`nest[2][1] = ${nest[2][1]}`);
//to get length of main array
console.log(`length of nest = ${nest.length}`);
//to gett length of any inner array
console.log(`length of nest[0] = ${nest[0].length}`);
console.log(`length of nest[1] = ${nest[1].length}`);
console.log(`length of nest[2] = ${nest[2].length}`);
