//while loop is amost like, for loop but its syntax is different 
//syntax->   while(condition){ 
//       body
//  };
//print numbers from 1-5 using while loop
let i=1;
while(i<=5){
    console.log(i);
    i++;
}

//print even numbers from 2-20 using while loop
let j=2;
while(j<=20){
    console.log(j);
    j+=2;
}

//print odd numbers between 1-49 using while loop
let k=1;
while(k<=50){
    if(k%2==1){
        console.log(k);
    }
    k++;
}